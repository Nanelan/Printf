#include "ft_printf.h"

int	ft_format(char const *s, int i)
{
	if (s[i] == '%' && (s[i + 1] == 'c' || s[i + 1] == 's'
			|| s[i + 1] == 'p' || s[i + 1] == 'd' || s[i + 1] == 'i'
			|| s[i + 1] == 'u' || s[i + 1] == 'x' || s[i + 1] == 'X'
			|| s[i + 1] == '%'))
		return (1);
	else
		return (0);
}
